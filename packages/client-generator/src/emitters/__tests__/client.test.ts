describe('emitModules — writer-facing module interface', () => {
  it('exposes the http module (runtime + auth helpers exported) and the endpoints', () => {
    const m = emitModules(apiModel({ services: [{ name: 'Default', operations: [operation()] }] }));
    expect(m.http('client')).toContain('export function __buildUrl(');
    expect(m.http('client')).toContain('export async function __request<T>(');
    expect(m.operations).toContain('export async function op(');
  });

  it('reports hasSchemas and fills the schemas module from the model types', () => {
    const withSchemas = emitModules(
      apiModel({ schemas: [namedSchema('Foo', { kind: 'scalar', scalar: 'string' })] })
    );
    expect(withSchemas.hasSchemas).toBe(true);
    expect(withSchemas.schemas).toContain('export type Foo = string;');

    const noSchemas = emitModules(apiModel({ schemas: [] }));
    expect(noSchemas.hasSchemas).toBe(false);
  });

  it('renderEndpoints renders a named subset; endpointImports wires the shared modules', () => {
    const m = emitModules(
      apiModel({
        services: [
          {
            name: 'Default',
            operations: [
              operation({
                name: 'getPet',
                path: '/pets/{id}',
                pathParams: [param('id', 'path', true)],
                successResponses: [
                  {
                    contentType: 'application/json',
                    schema: { kind: 'ref', name: 'Pet' },
                    status: 200,
                  },
                ],
              }),
            ],
          },
        ],
      })
    );
    const ops = [
      operation({
        name: 'getPet',
        path: '/pets/{id}',
        pathParams: [param('id', 'path', true)],
        successResponses: [
          { contentType: 'application/json', schema: { kind: 'ref', name: 'Pet' }, status: 200 },
        ],
      }),
    ];
    expect(m.renderEndpoints(ops, 'PetsService')).toContain('export async function getPet(');
    const imports = m.endpointImports(ops, 'client');
    expect(imports).toContain('from "./client.http.js"');
    expect(imports).toContain('import type { Pet } from "./client.schemas.js";');
    // `prefix` reaches one folder up for the tags-split layout.
    expect(m.endpointImports(ops, 'client', '../')).toContain('from "../client.http.js"');
  });

  it('publicReexport re-exports the public runtime values and config types', () => {
    const m = emitModules(apiModel());
    const reexport = m.publicReexport('client');
    expect(reexport).toContain(
      'export { ApiError, configure, setServerUrl, use } from "./client.http.js";'
    );
    expect(reexport).toContain('export type {');
  });
});

describe('SSE plumbing (single + multi-file)', () => {
  it('emitSingleFile emits the __sse runtime block and the sse aggregate for an SSE op', () => {
    const out = emitSingleFile(sseModel());
    expect(out).toContain('async function* __sse');
    expect(out).toContain('export const sse =');
  });

  it('emitSingleFile omits __sse and the sse aggregate when no op streams', () => {
    const out = emitSingleFile(
      apiModel({ services: [{ name: 'Default', operations: [operation()] }] })
    );
    expect(out).not.toContain('async function* __sse');
    expect(out).not.toContain('export const sse =');
  });

  it('emitModules http exports the __sse generator for an SSE model', () => {
    const m = emitModules(sseModel());
    expect(m.http('client')).toContain('export async function* __sse');
  });

  it('publicReexport re-exports ServerSentEvent + SseOptions only for an SSE model', () => {
    const sse = emitModules(sseModel()).publicReexport('client');
    const typeLine = sse.split('\n').find((l) => l.startsWith('export type {')) ?? '';
    expect(typeLine).toContain('ServerSentEvent');
    expect(typeLine).toContain('SseOptions');

    const plain = emitModules(apiModel()).publicReexport('client');
    expect(plain).not.toContain('ServerSentEvent');
    expect(plain).not.toContain('SseOptions');
  });

  it('endpointImports pulls __sse (value) + ServerSentEvent/SseOptions (types) + the event type', () => {
    const ops = [sseOperation()];
    const imports = emitModules(sseModel()).endpointImports(ops, 'client');
    expect(imports).toContain('__sse');
    expect(imports).toContain('type ServerSentEvent');
    expect(imports).toContain('type SseOptions');
    // The streamed event schema is imported from the schemas module.
    expect(imports).toContain('import type { Message } from "./client.schemas.js";');
    // An SSE-only file never references the regular request terminal or RequestOptions.
    expect(imports).not.toMatch(/\b__request\b/);
    expect(imports).not.toContain('RequestOptions');
  });

  it('a regular-only endpoints file imports no SSE symbols', () => {
    const ops = [
      operation({
        name: 'getPet',
        path: '/pets/{id}',
        pathParams: [param('id', 'path', true)],
        successResponses: [
          { contentType: 'application/json', schema: { kind: 'ref', name: 'Pet' }, status: 200 },
        ],
      }),
    ];
    const imports = emitModules(sseModel()).endpointImports(ops, 'client');
    expect(imports).not.toMatch(/\b__sse\b/);
    expect(imports).not.toContain('ServerSentEvent');
    expect(imports).not.toContain('SseOptions');
  });
});

describe('query-auth + TokenProvider threading', () => {
  const queryAuthModel = apiModel({
    securitySchemes: [{ kind: 'apiKeyQuery', key: 'ApiKeyQuery', paramName: 'api_key' }],
    services: [
      {
        name: 'Default',
        operations: [operation({ name: 'ping', path: '/p', security: ['ApiKeyQuery'] })],
      },
    ],
  });

  it('emitSingleFile merges __a.query into the URL for an apiKey-query op (end-to-end)', () => {
    const out = emitSingleFile(queryAuthModel, {});
    expect(out).toContain('...__a.query');
    expect(out).toContain('export type TokenProvider');
    expect(out).toContain('export function setApiKey(');
  });

  it('publicReexport type list includes TokenProvider; value list includes setBearer', () => {
    const bearerModel = apiModel({
      securitySchemes: [{ kind: 'bearer', key: 'Bearer' }],
    });
    const reexport = emitModules(bearerModel, {}).publicReexport('client');
    const valueLine = reexport.split('\n').find((l) => l.startsWith('export {')) ?? '';
    const typeLine = reexport.split('\n').find((l) => l.startsWith('export type {')) ?? '';
    expect(valueLine).toContain('setBearer');
    expect(typeLine).toContain('TokenProvider');
  });

  it('publicReexport value list includes setBasicAuth + apiKey setters across all in', () => {
    const mixed = apiModel({
      securitySchemes: [
        { kind: 'basic', key: 'Basic' },
        { kind: 'apiKeyQuery', key: 'Q', paramName: 'q' },
        { kind: 'apiKeyCookie', key: 'C', cookieName: 'c' },
      ],
    });
    const reexport = emitModules(mixed, {}).publicReexport('client');
    const valueLine = reexport.split('\n').find((l) => l.startsWith('export {')) ?? '';
    const typeLine = reexport.split('\n').find((l) => l.startsWith('export type {')) ?? '';
    expect(valueLine).toContain('setBasicAuth');
    // Two apiKey schemes of distinct `in` → distinct setters.
    expect(valueLine).toMatch(/setApiKey\w+/);
    expect(typeLine).toContain('TokenProvider');
  });

  it('renderEndpoints threads queryAuthKeys into the per-tag endpoint render', () => {
    const m = emitModules(queryAuthModel, {});
    const ops = [operation({ name: 'ping', path: '/p', security: ['ApiKeyQuery'] })];
    expect(m.renderEndpoints(ops, 'PingService')).toContain('...__a.query');
  });
});

describe('errorMode threading', () => {
  const resultModel = apiModel({
    services: [{ name: 'Default', operations: [operation({ name: 'ping', path: '/p' })] }],
  });

  it('emitSingleFile honors errorMode: result in runtime + operations', () => {
    const out = emitSingleFile(resultModel, { errorMode: 'result' });
    expect(out).toContain('__requestResult');
    expect(out).toContain('export type Result<');
    expect(out).toContain('Promise<Result<');
  });

  it('emitSingleFile defaults to throw mode (no result surface)', () => {
    const out = emitSingleFile(resultModel, {});
    expect(out).not.toContain('__requestResult');
    expect(out).not.toContain('export type Result<');
    expect(out).not.toContain('Promise<Result<');
  });

  it('emitModules publicReexport re-exports Result only in result mode', () => {
    const reexportLine = (out: string) =>
      out.split('\n').find((l) => l.startsWith('export type {')) ?? '';

    const result = emitModules(resultModel, { errorMode: 'result' }).publicReexport('client');
    expect(reexportLine(result)).toContain('Result');

    const thrown = emitModules(resultModel, {}).publicReexport('client');
    expect(reexportLine(thrown)).not.toContain('Result');
  });

  it('renderEndpoints emits result-mode operations (the per-tag layout path)', () => {
    const m = emitModules(resultModel, { errorMode: 'result' });
    const ops = [operation({ name: 'ping', path: '/p' })];
    const out = m.renderEndpoints(ops, 'PingService');
    expect(out).toContain('__requestResult<');
    expect(out).toContain('Promise<Result<');
    expect(out).not.toContain('__request<');
  });

  it('endpointImports pulls __requestResult in result mode, __request in throw mode', () => {
    const ops = [operation({ name: 'ping', path: '/p' })];

    const result = emitModules(resultModel, { errorMode: 'result' });
    const resultImports = result.endpointImports(ops, 'client');
    expect(resultImports).toContain('__requestResult');
    expect(resultImports).not.toContain('__request,');
    expect(resultImports).not.toMatch(/\b__request\b(?!Result)/);
    // Result-mode signatures reference `Result<…>`, so the endpoints file must
    // import the type from the http module.
    expect(resultImports).toContain('type Result');

    const thrown = emitModules(resultModel, {});
    const thrownImports = thrown.endpointImports(ops, 'client');
    expect(thrownImports).toMatch(/\b__request\b/);
    expect(thrownImports).not.toContain('__requestResult');
    expect(thrownImports).not.toContain('type Result');
  });

  it('endpointImports imports error-body schemas only in result mode', () => {
    // An op whose only named-schema reference is in its error response.
    const ops = [
      operation({
        name: 'ping',
        path: '/p',
        errorResponses: [
          {
            contentType: 'application/json',
            schema: { kind: 'ref', name: 'Problem' },
            status: 200,
          },
        ],
      }),
    ];

    // Result mode names the error body (`PingError = Problem`), so it imports `Problem`.
    expect(
      emitModules(resultModel, { errorMode: 'result' }).endpointImports(ops, 'client')
    ).toContain('Problem');
    // Throw mode never names the error body — importing it would trip `noUnusedLocals`.
    expect(emitModules(resultModel, {}).endpointImports(ops, 'client')).not.toContain('Problem');
  });
});

describe('multipart serialization helper (#5)', () => {
  const uploadModel = apiModel({
    services: [
      {
        name: 'Default',
        operations: [
          operation({
            name: 'upload',
            method: 'post',
            requestBody: {
              contentType: 'multipart/form-data',
              required: true,
              schema: {
                kind: 'object',
                properties: [
                  {
                    name: 'file',
                    schema: { kind: 'scalar', scalar: 'string', metadata: { format: 'binary' } },
                    required: true,
                  },
                ],
              },
            },
          }),
        ],
      },
    ],
  });

  it('emits __toFormData when an operation has a typed multipart body', () => {
    expect(emitSingleFile(uploadModel)).toContain('function __toFormData');
  });

  it('omits __toFormData when no operation needs it', () => {
    expect(emitSingleFile(apiModel())).not.toContain('__toFormData');
  });

  it('does not import __toFormData into the endpoints module (the runtime serializes)', () => {
    const ops = uploadModel.services[0].operations;
    // `__send` (http module) serializes a multipart body after the onRequest chain, so
    // `__toFormData` stays module-private there and the endpoints module never imports it.
    expect(emitModules(uploadModel, {}).endpointImports(ops, 'client')).not.toContain(
      '__toFormData'
    );
  });
});
