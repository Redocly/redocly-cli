import type { ApiModel, OperationModel } from '../../intermediate-representation/model.js';
import { emitModules, emitSingleFile, setupApply } from '../client.js';
import { apiModel, emitWithOp, namedSchema, operation, param } from './fixtures.js';

describe('typed ctx.operation (OperationContext narrowing)', () => {
  const tagged = apiModel({
    services: [
      {
        name: 'Default',
        operations: [operation({ name: 'listPets', path: '/pets', tags: ['Pets'] })],
      },
    ],
  });

  it('single-file narrows OperationContext to the operation unions', () => {
    const out = emitSingleFile(tagged, {});
    // The TS printer expands the type literal across lines.
    expect(out).toContain('id: OperationId;');
    expect(out).toContain('path: OperationPath;');
    expect(out).toContain('tags: OperationTag[];');
  });

  it('multi-file http module type-imports the operation unions from schemas', () => {
    const mods = emitModules(tagged, {});
    expect(mods.http('client')).toContain(
      'import type { OperationId, OperationPath, OperationTag } from "./client.schemas.js";'
    );
  });

  it('falls back to all-string OperationContext (and no import) when there are no operations', () => {
    const out = emitSingleFile(apiModel(), {});
    expect(out).toContain('id: string;');
    expect(out).toContain('tags: string[];');
    expect(out).not.toContain('id: OperationId;');
    expect(emitModules(apiModel(), {}).http('client')).not.toContain('from "./client.schemas.js"');
  });
});

describe('setupApply — baked publisher setup (--setup)', () => {
  const EXPR = '{ config: { serverUrl: "https://x" }, middleware: [] }';

  it('functions facade: declares __redoclySetup then applies it via configure/use', () => {
    const out = setupApply(EXPR, 'functions', false);
    expect(out).toContain(
      'const __redoclySetup: { config?: ClientConfig; middleware?: Middleware[] } = ' + EXPR + ';'
    );
    expect(out).toContain('configure(__redoclySetup.config ?? {});');
    expect(out).toContain('use(...(__redoclySetup.middleware ?? []));');
    expect(out).not.toContain('export const');
  });

  it('service-class facade: declaration only (no configure/use); exported when asked', () => {
    const out = setupApply(EXPR, 'service-class', true);
    expect(out).toContain('export const __redoclySetup');
    expect(out).not.toContain('configure(');
    expect(out).not.toContain('use(...');
  });

  it('single-file service-class constructor merges __redoclySetup into this.config', () => {
    const out = emitSingleFile(apiModel(), { facade: 'service-class', setup: EXPR });
    expect(out).toContain('...__redoclySetup.config');
    expect(out).toContain('this.config = {');
  });

  it('single-file functions facade appends the configure/use application', () => {
    const out = emitSingleFile(apiModel(), { setup: EXPR });
    expect(out).toContain('configure(__redoclySetup.config ?? {});');
  });

  it('multi-file: the http module carries the baked setup', () => {
    const mods = emitModules(apiModel(), { setup: EXPR });
    expect(mods.http('client')).toContain('const __redoclySetup');
    expect(mods.http('client')).toContain('configure(__redoclySetup.config ?? {});');
  });
});

/** An SSE operation streaming `Message` events (event-stream success + itemSchema $ref). */
function sseOperation(overrides: Partial<OperationModel> = {}): OperationModel {
  return operation({
    name: 'streamMessages',
    path: '/stream',
    successResponses: [
      {
        contentType: 'text/event-stream',
        status: 200,
        schema: { kind: 'unknown' },
        itemSchema: { kind: 'ref', name: 'Message' },
      },
    ],
    ...overrides,
  });
}

/** A model with one SSE op + the `Message` schema it streams. */
function sseModel(overrides: Partial<ApiModel> = {}): ApiModel {
  return apiModel({
    schemas: [namedSchema('Message', { kind: 'object', properties: [] })],
    services: [{ name: 'Default', operations: [sseOperation()] }],
    ...overrides,
  });
}

describe('emitSingleFile — top-level layout', () => {
  it('includes the generated-by header and ends with a newline', () => {
    const out = emitSingleFile(apiModel());
    expect(out.startsWith('// Generated by @redocly/client-generator')).toBe(true);
    expect(out.endsWith('\n')).toBe(true);
  });

  it('inlines the serverUrl as a mutable `let` binding so setServerUrl() can update it', () => {
    const out = emitSingleFile(apiModel({ serverUrl: 'https://api.example.com/v1' }));
    expect(out).toContain('let BASE = "https://api.example.com/v1";');
    expect(out).not.toContain('const BASE = ');
  });

  it('always exports a setServerUrl() helper that reassigns the BASE binding', () => {
    const out = emitSingleFile(apiModel({ serverUrl: 'https://api.example.com' }));
    expect(out).toContain('export function setServerUrl(url: string): void');
    expect(out).toMatch(/BASE\s*=\s*url/);
  });

  it('lets a caller override the spec-derived serverUrl via options.serverUrl', () => {
    const out = emitSingleFile(apiModel({ serverUrl: 'https://from-spec.example.com' }), {
      serverUrl: 'http://127.0.0.1:3101',
    });
    expect(out).toContain('let BASE = "http://127.0.0.1:3101";');
    expect(out).not.toContain('from-spec.example.com');
  });

  it('emits a JSDoc title with the title and version', () => {
    const out = emitSingleFile(apiModel({ title: 'My API', version: '2.0.0' }));
    expect(out).toContain('* My API (v2.0.0)');
  });

  it('appends multi-line descriptions in the JSDoc title block', () => {
    const out = emitSingleFile(apiModel({ title: 'D', description: 'line one\nline two' }));
    expect(out).toContain(' * line one');
    expect(out).toContain(' * line two');
  });
});

describe('emitSingleFile — service-class facade', () => {
  it('emits a Client class by default (name omitted)', () => {
    const out = emitSingleFile(
      apiModel({
        services: [{ name: 'Default', operations: [operation({ name: 'ping' })] }],
      }),
      { facade: 'service-class' }
    );
    expect(out).toContain('export class Client {');
    expect(out).toMatch(/\basync ping\(/);
  });

  it('honors a custom class name via options.name', () => {
    const out = emitSingleFile(
      apiModel({
        services: [{ name: 'Default', operations: [operation({ name: 'ping' })] }],
      }),
      { facade: 'service-class', name: 'CafeClient' }
    );
    expect(out).toContain('export class CafeClient {');
    expect(out).not.toContain('export class Client {');
  });

  it('shares the identical http + schemas modules with the functions facade (core unchanged)', () => {
    const model = apiModel({
      services: [{ name: 'Default', operations: [operation({ name: 'ping' })] }],
    });
    const fns = emitModules(model, { facade: 'functions' });
    const cls = emitModules(model, { facade: 'service-class' });
    // The facade changes only the endpoints; the shared http (runtime + auth) and
    // schemas (types + guards) modules are byte-identical across facades.
    expect(cls.http('client')).toBe(fns.http('client'));
    expect(cls.schemas).toBe(fns.schemas);
  });
});

describe('extension contract (ClientConfig)', () => {
  it('emits the ClientConfig + RequestContext types, the global __config, and configure()', () => {
    const out = emitSingleFile(apiModel());
    expect(out).toContain('export type ClientConfig = {');
    expect(out).toContain('export type RequestContext = {');
    expect(out).toContain('const __config: ClientConfig = {};');
    expect(out).toContain('export function configure(config: ClientConfig): void {');
    expect(out).toContain('Object.assign(__config, config);');
  });

  it('threads the global __config through functions (both __buildUrl and __request)', () => {
    const out = emitWithOp({ name: 'ping', path: '/p' });
    expect(out).toContain('__buildUrl(__config, `/p`)');
    // __request receives __config as its first argument.
    expect(out).toContain('return __request<void>(__config,');
  });

  it('passes the operation { id, path, tags } literal into the runtime call', () => {
    const out = emitWithOp({ name: 'ping', path: '/p' });
    expect(out).toContain('__request<void>(__config, { id: "ping", path: "/p", tags: [] }');
  });

  it('runtime resolves config.serverUrl ?? BASE and config.fetch ?? fetch', () => {
    const out = emitSingleFile(apiModel());
    expect(out).toContain('(config.serverUrl ?? BASE)');
    expect(out).toContain('const doFetch = config.fetch ?? fetch;');
  });

  it('runs the request through the composable middleware chain (single hooks + use())', () => {
    const out = emitSingleFile(apiModel());
    // The single config hooks are folded into one implicit first middleware.
    expect(out).toContain('function __middleware(config: ClientConfig): Middleware[]');
    expect(out).toContain('for (const mw of middleware)');
    expect(out).toContain('if (mw.onRequest)');
    // onResponse runs in reverse (onion).
    expect(out).toContain('for (let i = middleware.length - 1; i >= 0; i--)');
    // onError threads through each middleware.
    expect(out).toContain('error = await mw.onError(error as ApiError, context);');
    // The `use()` registrar is exported for the functions facade.
    expect(out).toContain('export function use(...middleware: Middleware[]): void');
  });

  it('service-class takes a ClientConfig constructor and threads this.config', () => {
    const out = emitSingleFile(
      apiModel({
        services: [
          {
            name: 'Default',
            operations: [operation({ name: 'ping', path: '/p' })],
          },
        ],
      }),
      { facade: 'service-class' }
    );
    expect(out).toContain('constructor(private readonly config: ClientConfig = {}) { }');
    expect(out).toContain('__buildUrl(this.config, `/p`)');
    expect(out).toContain('return __request<void>(this.config,');
  });
});

