import { apiModel, operation } from '../../emitters/__tests__/fixtures.js';
import { getGenerator } from '../index.js';
import { swrGenerator } from '../swr.js';

const SERVICES = [
  {
    name: 'Default',
    operations: [
      operation({ name: 'getPet', method: 'get', path: '/pet' }),
      operation({ name: 'createPet', method: 'post', path: '/pet' }),
    ],
  },
];

describe('swrGenerator', () => {
  it('emits one <stem>.swr.ts file beside the client with the header + hooks', () => {
    const files = swrGenerator({
      model: apiModel({ services: SERVICES }),
      outputPath: '/tmp/out/client.ts',
      outputMode: 'single',
      emit: {},
    });
    expect(files).toHaveLength(1);
    expect(files[0].path).toBe('/tmp/out/client.swr.ts');
    expect(files[0].content).toContain('// Generated by @redocly/openapi-typescript');
    expect(files[0].content).toContain('import useSWR from "swr"');
    expect(files[0].content).toContain('import useSWRMutation from "swr/mutation"');
    expect(files[0].content).toContain('export function useGetPet');
    expect(files[0].content).toContain('export function useCreatePet');
    expect(files[0].content).toContain('from "./client.js"');
  });

  it('emits nothing when the model has no operations', () => {
    const files = swrGenerator({
      model: apiModel({ services: [{ name: 'Default', operations: [] }] }),
      outputPath: '/tmp/out/client.ts',
      outputMode: 'single',
      emit: {},
    });
    expect(files).toEqual([]);
  });

  it('is registered under "swr"', () => {
    expect(getGenerator('swr').run).toBe(swrGenerator);
  });
});
