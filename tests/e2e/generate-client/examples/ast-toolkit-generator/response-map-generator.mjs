// A custom generator that builds its output as a real TypeScript AST with the
// `@redocly/client-generator/generate` toolkit â€” the same `ts.factory` + printer the
// built-in generators use â€” instead of concatenating strings. It emits
// `<output>.responses.ts`: a `ResponseShapes` type mapping every operation to the
// TypeScript type of its primary JSON success body. `schemaToTypeNode` does the
// schemaâ†’type mapping (refs, arrays, unions, formats) exactly as the sdk does, and the
// printer gets quoting and type syntax right for free.
//
// Plain ESM so the CLI imports it under bare `node`. Authored in TypeScript you would write:
//
//   import { defineGenerator } from '@redocly/client-generator';
//   import { printStatements, schemaToTypeNode, ts } from '@redocly/client-generator/generate';
//   export default defineGenerator({ name: 'response-map', requires: ['sdk'], run({ model, outputPath }) { â€¦ } });
import { printStatements, schemaToTypeNode, ts } from '@redocly/client-generator/generate';

const { factory } = ts;

export default {
  name: 'response-map',
  requires: ['sdk'],
  run({ model, outputPath }) {
    // Every operation with a JSON success body â€” a 204 or an image download has no entry.
    const withJsonBody = model.services
      .flatMap((service) => service.operations)
      .flatMap((op) => {
        const success = op.successResponses.find((r) =>
          r.contentType.toLowerCase().includes('json')
        );
        return success ? [{ name: op.name, schema: success.schema }] : [];
      });

    const responseShapes = factory.createTypeAliasDeclaration(
      [factory.createModifier(ts.SyntaxKind.ExportKeyword)],
      'ResponseShapes',
      undefined,
      factory.createTypeLiteralNode(
        withJsonBody.map(({ name, schema }) =>
          factory.createPropertySignature(undefined, name, undefined, schemaToTypeNode(schema))
        )
      )
    );

    // `schemaToTypeNode` renders a `$ref` as a bare type reference, so the module
    // type-imports every referenced schema name from the generated client.
    const referenced = [...new Set(withJsonBody.flatMap(({ schema }) => refNames(schema)))].sort();
    const importLine =
      referenced.length > 0
        ? `import type { ${referenced.join(', ')} } from './client.js';\n\n`
        : '';

    return [
      {
        path: outputPath.replace(/\.ts$/, '.responses.ts'),
        content:
          '// Generated by the response-map custom generator. Do not edit by hand.\n' +
          importLine +
          printStatements([responseShapes]) +
          '\n',
      },
    ];
  },
};

/** The named schemas a schema tree references (refs nested in arrays/objects/unions). */
function refNames(schema) {
  switch (schema.kind) {
    case 'ref':
      return [schema.name];
    case 'array':
      return refNames(schema.items);
    case 'object':
      return schema.properties.flatMap((property) => refNames(property.schema));
    case 'record':
      return refNames(schema.value);
    case 'union':
    case 'intersection':
      return schema.members.flatMap(refNames);
    default:
      return [];
  }
}
